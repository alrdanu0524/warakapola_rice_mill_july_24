    public void tbUpdate() {
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        rightRenderer.setHorizontalAlignment(JLabel.RIGHT);
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);

// Define number formats
        DecimalFormat qtyFormat = new DecimalFormat("0.000");
        NumberFormat subCostFormat = NumberFormat.getNumberInstance(Locale.US);
        subCostFormat.setMinimumFractionDigits(2);
        subCostFormat.setMaximumFractionDigits(2);

        boolean itemExists = false;

        for (int row = 0; row < dt.getRowCount(); row++) {
            if (dt.getValueAt(row, 0).equals(s_id)) {
                // Item already exists, update quantity and prices
                System.out.println("");
                if (updateType.equals("Free")) {
                    double existingFredd = Double.parseDouble(dt.getValueAt(row, 9).toString().replace(",", ""));
                    double newFree = existingFredd + d_free;
                    dt.setValueAt(subCostFormat.format(newFree), row, 9);

                } else if (updateType.equals("Warr")) {
                    String existingWarranty = dt.getValueAt(row, 14).toString().replace(",", "");
                    if (existingWarranty.equals("No")) {
                        dt.setValueAt(s_warranty, row, 14);
                    }
                } else if (updateType.equals("qty")) {

                    double existingQty = Double.parseDouble(dt.getValueAt(row, 6).toString().replace(",", ""));
                    double newQty = existingQty * d_qty;
                    dt.setValueAt(qtyFormat.format(newQty), row, 6);

//                    double existingCostSub = Double.parseDouble(dt.getValueAt(row, 10).toString().replace(",", ""));
                    double newCostSub = newQty * d_cost_sub;
                    dt.setValueAt(subCostFormat.format(newCostSub), row, 10);
//                d_cost_sub = 0.0;

                    double existingqty = Double.parseDouble(dt.getValueAt(row, 11).toString().replace(",", ""));
                    double dis = existingqty * newQty;
                    dt.setValueAt(subCostFormat.format(dis), row, 11);
                } else if (updateType.equals("dis")) {
                    double existingPrice = Double.parseDouble(dt.getValueAt(row, 3).toString().replace(",", ""));
                    String existingDis = dt.getValueAt(row, 13).toString().replace(",", "");
                    if (existingDis.equals("0") || existingDis.equals("0.0")) {
                        dt.setValueAt(d_val, row, 13);
                    }

                    if (d_val.contains("%")) {
                        d_val = d_val.replace("%", "");
                        double value = Double.parseDouble(d_val);
                        d_dis_amo = existingPrice * value / 100;

                    } else {

                        d_dis_amo = Double.parseDouble(d_val);
                    }
                    double newSale = existingPrice - d_dis_amo;
                    dt.setValueAt(subCostFormat.format(newSale), row, 3);
//                d_val = "0.0";
//                d_dis_amo = 0.0;
                    double existingPricex = Double.parseDouble(dt.getValueAt(row, 3).toString().replace(",", ""));
                    double existingqty = Double.parseDouble(dt.getValueAt(row, 6).toString().replace(",", ""));
                    double dis = existingqty * existingPricex;
                    dt.setValueAt(subCostFormat.format(dis), row, 11);

                }

                itemExists = true;
                break;
            }
        }

        if (!itemExists) {
            // Add new item
            Vector v = new Vector();
            v.add(s_id);
            v.add(s_code);
            v.add(s_name);
            v.add(subCostFormat.format(d_price));
            v.add(d_mrp);
            v.add(d_cost);
            v.add(qtyFormat.format(d_qty));
            v.add(s_type);
            v.add(s_sinhala);
            v.add(d_free);
            v.add(subCostFormat.format(d_cost_sub));
            v.add(subCostFormat.format(d_sub));
            v.add(s_discount);
            v.add(s_discount_amount);
            v.add(s_warranty);
            dt.addRow(v);

        }

        int lastRow = jTable1.getRowCount() - 1;

        if (lastRow >= 0) {
            jTable1.setRowSelectionInterval(lastRow, lastRow);
            jTable1.scrollRectToVisible(jTable1.getCellRect(lastRow, 0, true));
        }
// Apply renderers
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(3).setCellRenderer(rightRenderer);
            jTable1.getColumnModel().getColumn(6).setCellRenderer(rightRenderer);
            jTable1.getColumnModel().getColumn(11).setCellRenderer(rightRenderer);
        }
        qtys();
    }


