    private void find_product(String s_id) {
        try {
            ResultSet rs_stock = MC_DB.search_dataQuery("SELECT * FROM stock WHERE id = '" + s_id + "'");
            if (rs_stock.next()) {
                rs_stock.beforeFirst(); // Move the cursor back to the start of the result set
                rs_stock.next(); // Move the cursor to the first row
                s_name = rs_stock.getString("name");
                s_code = rs_stock.getString("code");
                s_id = rs_stock.getString("id");

                s_discount_amount = "0";
                d_mrp = Double.parseDouble(rs_stock.getString("market_price"));
                d_cost = Double.parseDouble(rs_stock.getString("buy_price"));

                if (s_price_type.equals("Retail")) {
                    d_price = Double.parseDouble(rs_stock.getString("sell_price"));
                    s_discount = Double.parseDouble(rs_stock.getString("sell_price"));
                } else if (s_price_type.equals("Wholesale")) {
                    d_price = Double.parseDouble(rs_stock.getString("wholesale"));
                    s_discount = Double.parseDouble(rs_stock.getString("wholesale"));

                } else if (s_price_type.equals("P1")) {
                    d_price = Double.parseDouble(rs_stock.getString("sell_price"));
                    s_discount = Double.parseDouble(rs_stock.getString("sell_price"));

                } else if (s_price_type.equals("P2")) {
                    d_price = Double.parseDouble(rs_stock.getString("sell_price"));
                    s_discount = Double.parseDouble(rs_stock.getString("sell_price"));

                } else if (s_price_type.equals("P3")) {
                    d_price = Double.parseDouble(rs_stock.getString("sell_price"));
                    s_discount = Double.parseDouble(rs_stock.getString("sell_price"));
                }

                d_avl_qty = Double.parseDouble(rs_stock.getString("qty"));
                d_re_order = Double.parseDouble(rs_stock.getString("reorder"));
                d_qty = 1.0;
                d_free = 0.0;
                s_sinhala = rs_stock.getString("name2");
                s_type = rs_stock.getString("type");
                s_warranty = "No";

                d_sub = d_qty * d_price;
                d_cost_sub = d_qty * d_cost;
                tbUpdate();
                txt_product.grabFocus();
                txt_product.setText("");
                if (d_avl_qty < d_re_order) {
                    lbl_notification.setText("This Item has Reached the Re-order Quantity.");
                } else {
                    lbl_notification.setText("");
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

